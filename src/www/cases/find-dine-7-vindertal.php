		<h2>Find dine 7 vindertal</h2>
		
		<div class="info">
			<div class="client">Lotto</div>
			<div class="published">13.10.2011</div>

		</div>
		
		<div class="article">
			<p>Der har altid været spekulation i hvordan man lige rammer de 7 tal der 
udløser den store Lotto gevinst. Vi syntes at det virkede oplagt at man 
fik lidt hjælp fra den heldige tvilling fra det nye Lotto univers. I et 
banner kunne brugeren styre tvillingens tennisketcher, og returnere 
Lottokugler der blev skudt afsted fra en boldmaskine. Hver gang brugeren
 ramte en Lottokugle, udløste det et tal. Spillet stoppede efter man 
havde samlet 7 lottotal, som man så kunne vælge at spille.</p><p><strong><a href="http://www.casefilm.dk/dia/designandcontent/danskespil/tennis/banner_930x600.htm">Se banneret her</a></strong></p>
