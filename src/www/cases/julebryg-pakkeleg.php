		<h2>Julebryg Pakkeleg</h2>
		
		<div class="info">
			<div class="client">Tuborg</div>
			<div class="published">13.11.2011</div>

		</div>
		
		<div class="article">
			<p>
                    	J-dag starter tidligere hvert år, og det bliver 
sværere og sværere at holde folk i julestemning helt fra start november 
til slut december. Derfor lavede vi en kampagne, der kunne aktivere 
kunderne, og holde samtalen gående omkring Tuborg Julebryg fra J-dag og 
frem til juleaften. Vi lavede en pakkeleg til Tuborgs Julebryg, hvor 
man kunne stjæle virtuelle pakker fra sine Facebook-venner ved at slå en sekser ved at ryste telefonen. Vi printede 
QR koder på alle julebryg etiketterne, så folk kunne scanne flasken med
 deres mobil, og hente app'en der gav dem adgang til spillet. Terningen blev kastet mere end 2 mio. gange.</p>
		</div>
		
