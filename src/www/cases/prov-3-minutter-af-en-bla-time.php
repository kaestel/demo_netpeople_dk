		<h2>Prøv 3 minutter af en Blå time</h2>
		
		<div class="info">
			<div class="client">Nykredit</div>
			<div class="published">13.08.2011</div>

		</div>
		
		<div class="article">
			<p>Det er svært at skabe involvering i produkter inden for bankverdenen. Men det er lykkedes for Nykredit: Med ”Blå time” har de gjort rådgivning mere appellerende og involveret brugerne i deres økonomi og bank.</p><p>Især har udviklingen af et videosite, der giver potentielle kunder en smagsprøve på deres Blå time, formået at aktivere en yderst modstræbende målgruppe: Videositet er så interessant, at 84 % ser deres video til ende – og heraf booker mere end 7 % et møde med Nykredit.</p><p>Samtidig kan Nykredit med tilfredshed konstatere, at Blå time nu er så cementeret et koncept, at potentielle kunder kommer ind fra gaden og beder om en Blå time.</p><p><a href="http://www.nykredit.dk/blaatime?intcsref=_privat_kampagne_blaatime_050112"><strong>Prøv selv 3 minutter af en blå time her</strong></a></p>
