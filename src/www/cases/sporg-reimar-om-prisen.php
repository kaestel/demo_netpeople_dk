		<h2>Spørg Reimar om prisen</h2>
		
		<div class="info">
			<div class="client">SAS</div>
			<div class="published">01.12.2011</div>

		</div>
		
		<div class="article">
			<p>Kan vi ikke bare få Uffe til at holde et prisskilt op? Det kunne vi 
ikke. I stedet blev det hans look-a-like, Johnny Reimar, der fik lov at 
afslutte SAS' klassiske filmunivers. Vi lavede en digital kampagne, hvor 
man kunne spørge Reimar om priserne på de billige flybilletter. Det var muligt at spørge Reimar om hvad som helst, og der gik hurtigt 
sport i at finde ud af hvad man kunne få Reimar til at svare på. Vi 
kunne monitorere folks spørgsmål real-time, og opdaterede løbende Reimars svar, så 
hans ordforråd voksede i løbet af kampagnen.</p><p><a href="http://www.casefilm.dk/dia/designandcontent/sas/reimar/banner_930x600.htm"><strong>Spørg selv Reimar her</strong></a></p>
		</div>
		
