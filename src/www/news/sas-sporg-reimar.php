	<div class="article">
		<p>SAS udkom i sommeren 2011 med sin sidste af 4 reklamefilm. Hvor det tidligere budskab var ''Så godt som hjemme'', blev det til ''Så godt som billigt''. Filmen havde Johnny Reimar i hovedrollen.</p>
		<p>NetPeople stod for den digitale del af kampagnen, og resultatet blev en løsning udover det sædvanlige. Et interaktivt banner gav brugerne mulighed for at spørge Johnny Reimar om billige priser til forskellige destinationer. Johnny Reimar kom straks med et svar. Hvis brugerne skrev en utraditionel destination, som SAS ikke flyver til, havde vi sikret, at Reimar i stedet svarede med en sjov kommentar.</p>
		<p>Kampagnen blev opdateret i real-time, så kommentarer til manglende destinationer på Twitter, Facebook og blogs blev monitoreret og gjort til en del af Reimars svarpulje, hvis nogen søgte på den samme destination igen. I alt var der 1,7 mio. interaktioner i banneret, og 15% brugte mere end 60 sekunder i banneret over kampagneperioden på 6 uger. Ligeledes udmøntrede kampagnen sig i en eksternt initieret Facebook side, hvor flittige folk postede deres mest kreative destinationer og svar fra Reimar.</p>
